# lista com string
frutas = ["laranja", "maca", "uva"]
print(frutas)

# lista vazia
frutas = []
print(frutas)

# cada letra é um elemento
letras = list("python")
print(letras)

# lista de 0 a 9
numeros = list(range(10))
print(numeros)

# lista mista
carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)
